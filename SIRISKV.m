function dydt = SIRISKV(t, y)
    gamma = 0.2;
    beta = 0.0016*[0 3 10 60 100]'*[0 3 10 60 100];
    
    %beta = [0 0 0 0 0; 0 0.65 2.15 12.9 21.5; 0 2.15 7.17 43.1 71.8; ...
    %          0 12.9 43.1 258 431; 0 21.5 71.8 431 718];
    
    n = [0.06 0.31 0.52 0.08 0.03]';
    dydt = beta * [y(1) y(2) y(3) y(4) y(5)]' .* (n - [y(1) y(2) y(3) y(4) y(5)]') - ...
          gamma * [y(1) y(2) y(3) y(4) y(5)]';
end